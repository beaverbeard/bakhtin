#!/usr/bin/env python3
"""
Бахтин — автоматический чекер черновика (golden-регрессия).

Ловит механизируемые провалы без LLM:
  1. Выдуманные числа (цифры + русские числительные словами), которых нет
     в allow-листе источника. Это W2 из baseline 25.06.
  2. Хард-баны AI-маркеров из базового профиля (regex).
  3. Длина вне диапазона формата.

Качество (хук/голос/угол/панч), верность исходнику и дрейф контента (W3)
этим НЕ проверяются — для них LLM-судья (см. README.md).

Usage:
  check.py --draft FILE --allow "две,третий" --min 1500 --max 2500
  check.py --draft FILE --allow-file BRIEF.md   # allow-числа из секции брифа

Вывод — краткий: счётчики + флаги. Полный текст не печатается (token hygiene).
Exit 0 — чисто, 1 — есть флаги.
"""
import argparse
import re
import sys

# Хард-баны базового профиля Бахтина (см. skills/bakhtin.md).
BANNED = [
    (r"не\s+просто\s+\w+,?\s+а\s+", "«не просто X, а Y»"),
    (r"не\s+только\s+\w+,?\s+но\s+и\s+", "«не только X, но и Y»"),
    (r"стоит\s+отметить", "«стоит отметить»"),
    (r"важно\s+понимать", "«важно понимать»"),
    (r"в\s+современном\s+мире", "«в современном мире»"),
    (r"в\s+эпоху\s+цифровизаци", "«в эпоху цифровизации»"),
    (r"играе\w+\s+ключев\w+\s+роль", "«играет ключевую роль»"),
    (r"можно\s+с\s+уверенностью\s+сказать", "«можно с уверенностью сказать»"),
    (r"подвод\w+\s+итог", "«подводя итог»"),
    (r"\bсинерги", "англицизм «синергия»"),
    (r"\bэкосистем", "англицизм «экосистема»"),
    (r"\bбесшовн", "промо «бесшовный»"),
    (r"\bинновацион", "промо «инновационный»"),
    (r"\bреволюционн", "промо «революционный»"),
    (r"\bуникальн", "промо «уникальный»"),
    (r"скажу\s+честно", "фальшивая искренность «скажу честно»"),
    (r"давайте\s+будем\s+честны", "фальшивая искренность"),
    (r"и\s+знаете\s+что", "фальшивая искренность «и знаете что»"),
    (r"таким\s+образом", "связка-маркер «таким образом»"),
]

# Числительные словами. EXACT — целым словом (\bw\b), чтобы «сто» не ловил
# «стоит»/«сторона», «два» — «дважды». STEM — префиксом (склоняемые основы).
# «один/одна/одно» намеренно НЕ включены: в русском это чаще артикль/
# местоимение («один из них», «ещё один раз»), а не выдуманная статистика.
# Фабрикации почти всегда ≥2 или конкретная цифра. Это снижает шум harness.
NUM_EXACT = [
    "ноль", "двое", "оба", "обе",
    "два", "две", "двух",
    "три", "трёх", "трех", "трое", "четыре", "четырёх", "четырех",
    "пять", "пяти", "шесть", "шести", "семь", "семи",
    "восемь", "восьми", "девять", "девяти", "десять", "десяти",
    "сорок", "сто", "ста", "двести", "триста",
    # Порядковые (первый/второй/третий…) НЕ включены: это чаще маркеры
    # последовательности («первый вопрос», «во-вторых»), а не выдуманная
    # статистика. Реальный риск — количественные. Порядковые из материала
    # кладутся в allow-лист; выдуманную порядковую цифру ловит LLM-слой.
]
NUM_STEM = [
    "одиннадцат", "двенадцат", "тринадцат", "четырнадцат", "пятнадцат",
    "шестнадцат", "семнадцат", "восемнадцат", "девятнадцат",
    "двадцат", "тридцат", "пятьдесят", "шестьдесят", "семьдесят",
    "восемьдесят", "девяност", "двести", "тысяч", "миллион", "миллиард",
    "половин", "четверт", "дюжин",
    # Собирательные (пятеро/пятерым) и кратные (вдвое/втрое/трижды) — W10.
    "пятер", "шестер", "семер", "восьмер", "девятер", "десятер",
    "вдво", "втро", "вчетвер", "впятер", "вшестер",
    "дважды", "трижды", "четырежды",
]


def norm(tok):
    return tok.lower().replace("ё", "е").strip()


def main():
    p = argparse.ArgumentParser()
    p.add_argument("--draft", required=True)
    p.add_argument("--allow", default="", help="разрешённые числа через запятую")
    p.add_argument("--min", type=int, default=0)
    p.add_argument("--max", type=int, default=10**9)
    a = p.parse_args()

    text = open(a.draft, encoding="utf-8").read()
    allow = {norm(x) for x in a.allow.split(",") if x.strip()}
    flags = []

    # 1. Числа — цифры. Нумерация тредов (1/8, 2/8) — легитимный формат theses,
    # вырезаем до скана.
    digit_text = re.sub(r"\b\d+\s*/\s*\d+\b", " ", text)
    for m in re.finditer(r"\d+(?:[.,]\d+)?", digit_text):
        if norm(m.group()) not in allow:
            flags.append(f"ЧИСЛО(цифра) «{m.group()}»")

    # 1b. Числа — словами. Сперва вырезаем гиперболы-идиомы («тысячу раз»,
    # «сто лет не виделись») — это фигуры речи, не выдуманные факты. (W8)
    HYPERBOLE = [
        r"тысяч[уи]\s+раз", r"сто\s+раз", r"в\s+сотый\s+раз",
        r"миллион\s+раз", r"сто\s+лет", r"тысяч[аи]\s+мелочей",
        r"за\s+тридевять", r"в\s+двух\s+словах", r"на\s+все\s+сто",
    ]
    low = norm(text)
    for h in HYPERBOLE:
        low = re.sub(h, " ", low)
    allow_ok = lambda w: any(al.startswith(w[:4]) or w.startswith(al[:4]) for al in allow)
    for w in NUM_EXACT:
        if re.search(r"\b" + w + r"\b", low) and not allow_ok(w):
            flags.append(f"ЧИСЛО(слово) «{w}»")
    for w in NUM_STEM:
        if re.search(r"\b" + w, low) and not allow_ok(w):
            flags.append(f"ЧИСЛО(слово) «{w}…»")

    # 2. Хард-баны.
    for pat, name in BANNED:
        if re.search(pat, text, re.IGNORECASE):
            flags.append(f"AI-МАРКЕР {name}")

    # 3. Длина (без пробелов считаем мягко — по символам с пробелами).
    n = len(text)
    if n < a.min:
        flags.append(f"ДЛИНА {n} < min {a.min}")
    if n > a.max:
        flags.append(f"ДЛИНА {n} > max {a.max}")

    # Дедуп.
    seen, uniq = set(), []
    for f in flags:
        if f not in seen:
            seen.add(f)
            uniq.append(f)

    print(f"[bakhtin-check] {a.draft}: длина {n}, флагов {len(uniq)}")
    for f in uniq:
        print(f"  ⚠ {f}")
    sys.exit(1 if uniq else 0)


if __name__ == "__main__":
    main()
